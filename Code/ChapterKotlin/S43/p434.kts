#!/usr/bin/env kotlin

//循环while
//基本语法:
while (条件) {
        // 执行代码
    }

//示例 ：while 基本使用
var i = 1
while (i <= 5) {
        println(i)
        i++
    }
// 输出：1 2 3 4 5

//示例 ：do-while 语句
var j = 1
do {
        println(j)
        j++
    } while (j <= 5)
// 输出：1 2 3 4 5


